    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed

        int Oper, Nume1, Nume2, Deno1, Deno2, Ente1, Ente2;
        clase Mixt1, Mixt2, Mixt3 = null;

        if (txtPrimerEntero.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite El Valor Entero", "Error", JOptionPane.ERROR_MESSAGE);
            txtPrimerEntero.requestFocusInWindow();
        } else if (txtDenominadorUno.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite El Denominador", "Error", JOptionPane.ERROR_MESSAGE);
            txtDemominadorDos.requestFocusInWindow();
        } else if (txtNumeroUno.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite El Numerador", "Error", JOptionPane.ERROR_MESSAGE);
            txtNumeroUno.requestFocusInWindow();
        } else if (txtNumeroDos.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite El Numerador", "Error", JOptionPane.ERROR_MESSAGE);
            txtEnteroDos.requestFocusInWindow();
        } else if (txtDemominadorDos.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite El Denominador", "Error", JOptionPane.ERROR_MESSAGE);
            txtDemominadorDos.requestFocusInWindow();
        } else if (txtEnteroDos.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite El Valor Entero", "Error", JOptionPane.ERROR_MESSAGE);
            txtEnteroDos.requestFocusInWindow();
        } else {
            Oper = cmbOper.getSelectedIndex();
            Nume1 = Integer.parseInt(txtNumeroUno.getText());
            Ente1 = Integer.parseInt(txtPrimerEntero.getText());
            Deno1 = Integer.parseInt(txtDenominadorUno.getText());
            Nume2 = Integer.parseInt(txtNumeroDos.getText());
            Ente2 = Integer.parseInt(txtEnteroDos.getText());
            Deno2 = Integer.parseInt(txtDemominadorDos.getText());
            try {
                Mixt1 = new clase(Nume1, Deno1, Ente1);
                Mixt2 = new clase(Nume2, Deno2, Ente2);
                switch (Oper) {
                    case 0:
                        Mixt3 = Mixt1.Suma(Mixt2);
                        break;
                    case 1:
                        Mixt3 = Mixt1.Resta(Mixt2);
                        break;
                    case 2:
                        Mixt3 = Mixt1.Multiplicacion(Mixt2);
                        break;
                    case 3:
                        Mixt3 = Mixt1.Division(Mixt2);
                        break;
                }
                txtNumeroTres.setText("" + Mixt3.getNumerador());
                txtDenominadorTres.setText("" + Mixt3.getDenominador());
                txtEnteroTres.setText("" + Mixt3.getNumero_Entero());
            } catch (DenominadorCeroException e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        cmdCalcular.setEnabled(true);
        cmdLimpiar.setEnabled(true);
        cmdFraccionarios.setEnabled(true);
    }//GEN-LAST:event_cmdCalcularActionPerformed

