    private void cmdFraccionariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdFraccionariosActionPerformed

        int Nume, Deno, Ente, CFracc, CDeno;

        Nume = Integer.parseInt(txtNumeroTres.getText());
        Deno = Integer.parseInt(txtDenominadorTres.getText());
        Ente = Integer.parseInt(txtEnteroTres.getText());
        CFracc = Ente * Deno + Nume;
        CDeno = Deno;

        txtNumerador1.setText("" + CFracc);
        txtDemoninador.setText("" + CDeno);
        cmdLimpiar.setEnabled(true);
        cmdCalcular.setEnabled(false);
        cmdFraccionarios.setEnabled(false);
        txtPrimerEntero.requestFocusInWindow();

    }//GEN-LAST:event_cmdFraccionariosActionPerformed

